    private void initOthers() {
        querySchemePanel = new QuerySchemePanel(empAnalysePara.getQuery_class());
        querySchemePanel.setQueryScheme(empAnalysePara.getQueryScheme());
        jPanel2.add(querySchemePanel, BorderLayout.CENTER);
        rebuild();
    }

    public void rebuild() {
        jPanel3.removeAll();
        if ("部门对比".equals(empAnalysePara.getCommAnalyseScheme().getScheme_type())) {
            jtpMain.remove(1);            
        } else {
            if (querySchemePanel2 == null) {
                querySchemePanel2 = new QuerySchemePanel2();
                querySchemePanel2.setQueryScheme(empAnalysePara.getQueryScheme());
            }
            jPanel3.add(querySchemePanel2, BorderLayout.CENTER);
        }
        jPanel3.updateUI();
    }

